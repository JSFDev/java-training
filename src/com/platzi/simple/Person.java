package com.platzi.simple;

/**
 * Simple object
 */
public class Person {
    private byte year;
    private byte height;
    private short monthSalary;
    private int yearSalary;

    public Person(byte year, byte height, short monthSalary, int yearSalary) {
        this.year = year;
        this.height = height;
        this.monthSalary = monthSalary;
        this.yearSalary = yearSalary;
    }
}
